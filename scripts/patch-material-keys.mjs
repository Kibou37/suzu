/**
 * Patches materialKey into suzuki-viewer-catalog.ts without re-downloading swatches.
 */
import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const tmpDir = path.join(root, '.tmp-suzuki');
const catalogFile = path.join(root, 'frontend', 'src', 'data', 'suzuki-viewer-catalog.ts');

const MODELS = [
  { slug: 'vitara', viewerSlug: 'vitara', gradeFolder: 'GLX' },
  { slug: 'jimny', viewerSlug: 'jimny', gradeFolder: 'GLX' },
  { slug: 'swift', viewerSlug: 'swift', gradeFolder: 'GL' },
  { slug: 's-cross', viewerSlug: 's-cross', gradeFolder: 'GLX' },
];

function readJsonFile(filePath) {
  const raw = fs.readFileSync(filePath, 'utf8').replace(/^\uFEFF/, '');
  return JSON.parse(raw);
}

function loadMaterials(model) {
  const localPath = path.join(tmpDir, `${model.slug}-materials.json`);
  if (!fs.existsSync(localPath)) {
    const url = `https://www.globalsuzuki.com/automobile/lineup/${model.viewerSlug}/viewer/assets/setting/${model.gradeFolder}/${model.gradeFolder}_materials.json`;
    execSync(`curl.exe -sL -m 90 "${url}" -o "${localPath}"`, { stdio: 'pipe' });
  }
  return readJsonFile(localPath);
}

function buildMap(materialsRaw) {
  const map = new Map();
  for (const entry of materialsRaw.MATERIALS?.EXT ?? []) {
    if (entry.layer !== 0) continue;
    const bc = entry.items?.find((item) => item.startsWith('BC_'));
    if (bc && entry.material) map.set(bc, entry.material);
  }
  return map;
}

const source = fs.readFileSync(catalogFile, 'utf8');
const catalogMatch = source.match(
  /export const suzukiViewerCatalog[^=]*=\s*(\{[\s\S]*?\n\})\s*as const;/,
);

if (!catalogMatch) {
  console.error('Could not parse catalog file');
  process.exit(1);
}

const catalog = eval(`(${catalogMatch[1]})`);

for (const model of MODELS) {
  const materials = loadMaterials(model);
  const map = buildMap(materials);
  for (const color of catalog[model.slug]?.bodyColors ?? []) {
    if (color.thumbnail && map.has(color.thumbnail)) {
      color.materialKey = map.get(color.thumbnail);
    }
  }
  console.log(`${model.slug}: patched ${catalog[model.slug].bodyColors.filter((c) => c.materialKey).length} colors`);
}

const fileContent = `/**
 * Colours and options sourced from Global Suzuki 360° viewer JSON.
 * @see https://www.globalsuzuki.com/automobile/
 * Generated by scripts/extract-suzuki-viewer-colors.mjs — do not edit manually.
 */
import type { ConfiguratorData } from './demo-configurator';

export type SuzukiViewerColor = {
  id: string;
  name: string;
  thumbnail: string;
  swatch?: string;
  materialKey?: string;
  price: number;
};

export type SuzukiViewerOption = {
  id: string;
  name: string;
  description?: string;
  price: number;
  category: string;
};

export const suzukiViewerCatalog: Record<string, ConfiguratorData> = ${JSON.stringify(catalog, null, 2)} as const;

export function getSuzukiViewerData(modelSlug: string): ConfiguratorData | undefined {
  return suzukiViewerCatalog[modelSlug];
}
`;

fs.writeFileSync(catalogFile, fileContent);
console.log('Done');
